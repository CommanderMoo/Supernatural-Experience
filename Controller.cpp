#include "Controller.h"
#include "RandomSen.h"

void Controller::playerTurn()
{
	RandomSen randomsen;


	for (int i = 0; i < 1; i++)
	{
		randomsen.q();
	}
	for (int i = 0; i < 1; i++)
	{
		randomsen.w();
	}
	for (int i = 0; i < 1; i++)
	{
		randomsen.e();
	}
	for (int i = 0; i < 1; i++)
	{
		randomsen.r();
	}

}
